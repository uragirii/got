func TestCommit(t *testing.T) {

	t.Run("parses the commit correctly from SHA", func(t *testing.T) {
		t.Setenv("GIT_DIR", ".git")

		objSha, _ := sha.FromString(DATA_SHA_STR)

		mapFs := fstest.MapFS{
			"objects/01/1bf1e3d368c58d3fd7ea584d69a9acb6b21c13": {Data: TREE_COMPRESSED_BYTES},
		}

		objPath, _ := objSha.GetObjPath()

		mapFs[objPath] = &fstest.MapFile{Data: DATA_COMPRESSED_BYTES}

		fs := fstest.MapFS(mapFs)

		commit, err := commit.FromSHA(objSha, fs)

		if err != nil {
			t.Error(err)
		}

		testutils.AssertString(t, "SHA", DATA_SHA_STR, commit.GetSHA().String())
		testutils.AssertString(t, "string", TEST_COMMIT_STR, commit.String())
		testutils.AssertString(t, "raw string", TEST_COMMIT_STR, commit.String())

		commit.CalculateSha()

		testutils.AssertString(t, "commit SHA", objSha.String(), commit.GetSHA().String())

		testutils.AssertString(t, "object type", string(object.CommitObj), string(commit.GetObjType()))

		var buffer bytes.Buffer

		commit.Write(&buffer)

		compressedBytes := buffer.Bytes()

		testutils.AssertBytes(t, "raw bytes", DATA_COMPRESSED_BYTES, compressedBytes)

	})
}
